function ConverterProximaCentauri() {
  //Pega o valor pelo id indicado no html que receberá alteração do js
  var valorElemento = document.getElementById("valor1");
  //Puxa o que está inserido na input
  var valorzinho = valorElemento.value;
  //Converte de string para decimal(Float)
  var valorTerraNumerico = parseFloat(valorzinho);
  //Conversão para anos-luz
  //var valorDaProximaCentauri = valorTerraNumerico * 9460536068.016;
  var valorEmAnosLuz = 4.24;
  var valorEmKMH = valorEmAnosLuz / 9.46;

  //PUXA o valor referência pelo id no HTML
  var elementoValorConvertido = document.getElementById("valorConvertido");
  //Variávem com a mensagem e valor convertido
  var valorConvertido =
    "A Próxima Centauri está a " +
    valorEmAnosLuz +
    " anos-luz distante, sendo " +
    valorEmKMH +
    "km/h longe da Terra";
  //EXIBIDOR que colocar dentro do elemento <></> no HTML
  elementoValorConvertido.innerHTML = valorConvertido;

  //Impressão
  console.log(valorConvertido);
}

